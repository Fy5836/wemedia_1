package com.wemedia.component;

import org.springframework.beans.BeansException;
import org.springframework.context.ApplicationContext;
import org.springframework.context.ApplicationContextAware;
import org.springframework.stereotype.Component;

/**
 * 今天在一个utils包下的文件中写一个方法的时候想去使用@autowired注入一些对象，但发现不可以直接使用
 * @Autowired，因为方法是static，方法中使用的对象也必须是static，但正常情况下@Autowired无法注入
 * 静态的bean,于是发现项目中用到了springContextHolder,通过使用这个方法就可以拿到静态的contants对象
 * ，从而获取到其中的变量等内容。
 */
//那些没有归入spring框架管理的类却要调用spring容器中的bean提供的工具类。
@Component
public class SpringContextHolder implements ApplicationContextAware {

    private static ApplicationContext appContext = null;

    /**
     * 通过name获取 Bean.
     *
     * @param name
     * @return
     */
    public static Object getBean(String name) {
        return appContext.getBean(name);

    }

    /**
     * 通过class获取Bean.
     *
     * @param clazz
     * @param <T>
     * @return
     */
    public static <T> T getBean(Class<T> clazz) {
        return appContext.getBean(clazz);
    }

    /**
     * 通过name,以及Clazz返回指定的Bean
     *
     * @param name
     * @param clazz
     * @param <T>
     * @return
     */
    public static <T> T getBean(String name, Class<T> clazz) {
        return appContext.getBean(name, clazz);
    }

    @Override
    public void setApplicationContext(ApplicationContext applicationContext) throws BeansException {
        if (appContext == null) {
            appContext = applicationContext;
        }
    }
}
